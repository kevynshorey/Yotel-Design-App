'use client'

import { useCallback, useEffect, useRef, useState, useTransition } from 'react'
import { useRouter } from 'next/navigation'
import { FileDown } from 'lucide-react'
import { Viewer3D } from '@/components/viewer/viewer-3d'
import { ViewerControls } from '@/components/viewer/viewer-controls'
import { OptionsSidebar } from '@/components/design/options-sidebar'
import { GeneratorControls } from '@/components/design/generator-controls'
import { MetricsPanel } from '@/components/design/metrics-panel'
import { ScoringPanel } from '@/components/design/scoring-panel'
import { AmenityPanel } from '@/components/design/amenity-panel'
import { generateAll } from '@/engine/generator'
import { useDesign } from '@/context/design-context'

const MODULE_ROUTES = ['/design', '/planning', '/finance', '/dataroom', '/invest'] as const

function GeneratingOverlay() {
  return (
    <div className="absolute inset-0 z-20 flex items-center justify-center bg-slate-950/60 backdrop-blur-sm">
      <div className="text-center">
        <div className="mx-auto mb-3 h-8 w-8 animate-spin rounded-full border-2 border-sky-400 border-t-transparent" />
        <p className="text-sm font-medium text-sky-400">Generating design options...</p>
        <p className="mt-1 text-xs text-slate-500">Evaluating 2,880 combinations</p>
      </div>
    </div>
  )
}

export default function DesignPage() {
  const { options, selectedOption, setOptions, selectOption } = useDesign()
  const [isPending, startTransition] = useTransition()
  const hasAutoGenerated = useRef(false)
  const router = useRouter()

  // Viewer control state
  const [activePreset, setActivePreset] = useState('3D')
  const [activeBasemap, setActiveBasemap] = useState('Google')
  const [showBoundaries, setShowBoundaries] = useState(true)
  const [showAmenities, setShowAmenities] = useState(true)
  const [explodedView, setExplodedView] = useState(false)

  const handleGenerate = useCallback(() => {
    startTransition(() => {
      const generated = generateAll(40)
      setOptions(generated)
      if (generated.length > 0) selectOption(generated[0].id)
    })
  }, [setOptions, selectOption])

  const handleExportReport = useCallback(() => {
    if (!selectedOption) return
    localStorage.setItem('yotel-selected-option', JSON.stringify(selectedOption))
    window.open('/report', '_blank')
  }, [selectedOption])

  // Auto-generate on first mount if no options exist
  useEffect(() => {
    if (options.length === 0 && !hasAutoGenerated.current) {
      hasAutoGenerated.current = true
      handleGenerate()
    }
  }, [options.length, handleGenerate])

  // Keyboard shortcuts
  useEffect(() => {
    function handleKeyDown(e: KeyboardEvent) {
      // Ignore if user is typing in an input or textarea
      const tag = (e.target as HTMLElement)?.tagName
      if (tag === 'INPUT' || tag === 'TEXTAREA' || tag === 'SELECT') return

      // 1-5: Switch modules
      const digit = parseInt(e.key, 10)
      if (digit >= 1 && digit <= 5) {
        router.push(MODULE_ROUTES[digit - 1])
        return
      }

      // G: Generate
      if (e.key === 'g' || e.key === 'G') {
        if (!isPending) handleGenerate()
        return
      }

      // Escape: Deselect
      if (e.key === 'Escape') {
        selectOption(null)
        return
      }

      // Arrow keys: Navigate options
      if ((e.key === 'ArrowUp' || e.key === 'ArrowDown') && options.length > 0) {
        e.preventDefault()
        const currentIndex = selectedOption
          ? options.findIndex((o) => o.id === selectedOption.id)
          : -1

        let nextIndex: number
        if (e.key === 'ArrowDown') {
          nextIndex = currentIndex < options.length - 1 ? currentIndex + 1 : 0
        } else {
          nextIndex = currentIndex > 0 ? currentIndex - 1 : options.length - 1
        }
        selectOption(options[nextIndex].id)
      }
    }

    window.addEventListener('keydown', handleKeyDown)
    return () => window.removeEventListener('keydown', handleKeyDown)
  }, [router, isPending, handleGenerate, selectOption, options, selectedOption])

  return (
    <div className="flex h-full">
      {/* Main viewport */}
      <div className="relative flex-1">
        <Viewer3D
          selectedOption={selectedOption}
          className="h-full w-full"
          activePreset={activePreset}
          activeBasemap={activeBasemap}
          showBoundaries={showBoundaries}
          showAmenities={showAmenities}
          explodedView={explodedView}
        />

        {/* Viewer controls overlay */}
        <ViewerControls
          activePreset={activePreset}
          activeBasemap={activeBasemap}
          showBoundaries={showBoundaries}
          showAmenities={showAmenities}
          explodedView={explodedView}
          onCameraChange={setActivePreset}
          onBasemapChange={setActiveBasemap}
          onToggleBoundaries={() => setShowBoundaries((v) => !v)}
          onToggleAmenities={() => setShowAmenities((v) => !v)}
          onToggleExploded={() => setExplodedView((v) => !v)}
        />

        {/* Loading overlay */}
        {isPending && <GeneratingOverlay />}

        {/* Floating overlays */}
        <MetricsPanel option={selectedOption} />
        <ScoringPanel option={selectedOption} />
        <GeneratorControls onGenerate={handleGenerate} isGenerating={isPending} />
        <AmenityPanel amenities={selectedOption?.amenities} />

        {/* Export Report button */}
        {selectedOption && (
          <button
            onClick={handleExportReport}
            title="Export Feasibility Report (PDF)"
            className="absolute bottom-4 right-4 z-20 flex items-center gap-2 rounded-lg bg-[#0f172a] px-3 py-2 text-xs font-medium text-white shadow-lg transition-colors hover:bg-[#1e293b]"
          >
            <FileDown size={14} />
            Export Report
          </button>
        )}
      </div>

      {/* Right sidebar */}
      <OptionsSidebar
        options={options}
        selectedId={selectedOption?.id ?? null}
        onSelect={selectOption}
      />
    </div>
  )
}
